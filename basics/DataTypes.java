// int -> short for integer
// int data type only allows values between -2,147,483,648 and 2,147,483,647
// boolean -> that can only be true or false
// char -> data type is used to represent single characters
// all char values must be enclosed in single quotes


public class DataTypes {
    public static void main(String[] args) {
        int integer = 2147483647;  // Example of integer data type
        boolean isTrue = true;  // Example of boolean data type
        char firstLetter = 'C';  // Example of char data type

        System.out.println(integer);  // increment the value of integer by one
                                      // to get an error
        System.out.println(isTrue);
        System.out.println(firstLetter);

    }
}
