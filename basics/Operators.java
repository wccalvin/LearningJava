// Relational operators: < > <= >=
// Equality operators: == !=

public class Operators {
    public static void main(String[] args) {
        System.out.println(10 > 5);  // returns true
        System.out.println(10 == 5);  // returns false
        System.out.println(10 != 5);  // returns true
        System.out.println(10 < 5);  // returns false
        System.out.println(10 >= 5);  // returns true
        System.out.println(10 <= 5);  // returns false
    }
}
