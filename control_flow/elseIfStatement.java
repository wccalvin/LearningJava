public class elseIfStatement {
    public static void main(String[] args) {
        int value = 2;
        if (value > 10) {
            System.out.println("Great score.");
        } else if (value > 1) {
            System.out.println("Well.. not that bad.");
        } else {
            System.out.println("quite bad.");
        }
    }
}
